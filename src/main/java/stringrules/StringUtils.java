package main.java.stringrules;

/**
 * Class of utils used to make the string parsing easier. 
 *
 */
public class StringUtils {

	/** 
	 * Checks if the character in parameters is equal to 3.
	 * @param the character
	 * @return Boolean.TRUE or Boolean.FALSE
	 */
	public static Boolean is3(char c) {
		return c == '3';
	}	
	
	/** 
	 * Checks if the character in parameters is equal to 5.
	 * @param the character
	 * @return Boolean.TRUE or Boolean.FALSE
	 */
	public static Boolean is5(char c) {
		return c == '5';
	}	
	
	/** 
	 * Checks if the character in parameters is equal to 7.
	 * @param the character
	 * @return Boolean.TRUE or Boolean.FALSE
	 */
	public static Boolean is7(char c) {
		return c == '7';
	}
	
	/** 
	 * Checks if the character in parameters is equal to 0.
	 * @param the character
	 * @return Boolean.TRUE or Boolean.FALSE
	 */
	public static Boolean is0(char c) {
		return c == '0';
	}
}
